"""
Program 1:
Print the 4th element from the beginning
and the 4th element from the end of a tuple.
"""

# Creating a sample tuple
numbers = (10, 20, 30, 40, 50, 60, 70, 80)

# Accessing the 4th element (index starts from 0)
print("4th element from the beginning:", numbers[3])

# Accessing the 4th element from the end
print("4th element from the end:", numbers[-4])